export function getNpcDialogForQuest(quest) {
  const archetype = guideArchetype(quest);
  const content = GUIDE_ARCHETYPES[archetype];
  const phrases = buildPhrases(quest);
  const antiPattern = {
    conversation: "Начать с длинного объяснения своего волнения",
    group: "Войти в разговор без паузы и разрешения",
    position: "Сразу обесценить чужое мнение",
    request: "Попросить обо всём сразу без контекста",
    invitation: "Сказать: «Надо бы как-нибудь встретиться»",
    appreciation: "Спрятать прямую фразу за шуткой",
    pause: "Заполнить паузу ответом за собеседника"
  }[archetype];
  const feedback = {
    conversation: "Убери объяснение напряжения. Представься и переведи внимание на общий контекст.",
    group: "Сначала дождись паузы и запроси согласие. Открытая группа всё равно имеет право отказать.",
    position: "Твёрдость не требует нападения. Назови свой тезис и одну причину без оценки человека.",
    request: "Сузь запрос до одного вопроса. Человеку должно быть легко ответить коротко или отказаться.",
    invitation: "Сделай предложение конкретным: действие, место, время и право сказать «нет».",
    appreciation: "Скажи конкретно и остановись. Шутка в конце может обесценить искреннюю фразу.",
    pause: "Не спасай тишину. Дай человеку время сформулировать собственный ответ."
  }[archetype];
  const npcReply = {
    conversation: "Привет. Да, я здесь впервые — пришёл из-за основной темы.",
    group: "Да, присоединяйся. Мы как раз обсуждаем программу.",
    position: "Понял твою причину. Я вижу иначе, но твоя позиция ясна.",
    request: "Да, могу назвать один вариант, с которого стоит начать.",
    invitation: "Теперь план понятен. Я могу проверить время и дать прямой ответ.",
    appreciation: "Спасибо. Приятно, что ты заметил именно это.",
    pause: "Хороший вопрос. Дай подумать… Для меня главное вот что."
  }[archetype];

  return {
    id: `npc_${quest.id}`,
    title: `Репетиция: ${quest.category}`,
    startNodeId: "start",
    nodes: {
      start: {
        speaker: "coach",
        text: `Сценарий квеста: ${quest.questText} Выбери первый ход.`,
        options: [
          { label: phrases[0], next: "response" },
          { label: antiPattern, next: "retry" }
        ]
      },
      retry: {
        speaker: "coach",
        text: feedback,
        options: [{ label: phrases[0], next: "response" }]
      },
      response: {
        speaker: "npc",
        text: npcReply,
        options: [
          { label: phrases[1], next: "success" },
          { label: content.exit, next: "safe_exit" }
        ]
      },
      safe_exit: {
        speaker: "coach",
        text: "Чистый выход тоже засчитывается: ты заметил границы и сам спокойно завершил контакт.",
        success: true,
        options: []
      },
      success: {
        speaker: "coach",
        text: `Репетиция готова. Основная опора: ${content.steps[1]}`,
        success: true,
        options: []
      }
    }
  };
}

function buildPhrases(quest) {
  if (quest.category === "Благодарность") {
    return ["Спасибо, хорошего дня.", "Спасибо, вы очень помогли.", "Благодарю, это было удобно."];
  }
  if (quest.category === "Рекомендация") {
    return ["Что бы вы посоветовали?", "Какой вариант вы бы выбрали?", "Есть что-то спокойное и проверенное?"];
  }
  if (quest.category === "Легкий комплимент") {
    return ["Классный принт.", "У вас интересная книга.", "Очень приятная чашка."];
  }
  if (quest.category === "Уточняющий вопрос") {
    return ["Подскажите, пожалуйста?", "Можно уточнить одну вещь?", "Где лучше посмотреть?"];
  }
  if (quest.category === "Знакомство с соседом") {
    return ["Привет, я здесь рядом работаю.", "Здравствуйте, я на этой встрече впервые.", "Я ненадолго представлюсь: меня зовут..."];
  }
  if (quest.category === "Shadow") {
    return ["Я могу попробовать позже.", "Один маленький шаг уже считается.", "Сегодня достаточно подготовки."];
  }
  return GUIDE_ARCHETYPES[guideArchetype(quest)].phrases;
}
